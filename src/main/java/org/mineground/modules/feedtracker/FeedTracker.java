package org.mineground.modules.feedtracker;

public class FeedTracker {
    private static String SubversionFeedUrl = "http://code.google.com/feeds/p/lvm/svnchanges/basic";
    private static String IssueFeedUrl = "http://code.google.com/feeds/p/lvm/issueupdates/basic";
    
    public FeedTracker() {
        
    }

	public static String getSubversionFeedUrl() {
		return SubversionFeedUrl;
	}

	public static void setSubversionFeedUrl(String subversionFeedUrl) {
		SubversionFeedUrl = subversionFeedUrl;
	}

	public static String getIssueFeedUrl() {
		return IssueFeedUrl;
	}

	public static void setIssueFeedUrl(String issueFeedUrl) {
		IssueFeedUrl = issueFeedUrl;
	}
    
    
}
